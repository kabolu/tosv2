public class SurveyResp {

	private Integer srSuRefno = null;
	private Integer srSqRefno = null;
	private String srValue = null;
	private String srActInd = null;

	public Integer getSrSuRefno() {
		return srSuRefno;
	}

	public void setSrSuRefno(Integer srSuRefno) {
		this.srSuRefno = srSuRefno;
	}

	public Integer getSrSqRefno() {
		return srSqRefno;
	}

	public void setSrSqRefno(Integer srSqRefno) {
		this.srSqRefno = srSqRefno;
	}

	public String getSrValue() {
		return srValue;
	}

	public void setSrValue(String srValue) {
		this.srValue = srValue;
	}

	public String getSrActInd() {
		return srActInd;
	}

	public void setSrActInd(String srActInd) {
		this.srActInd = srActInd;
	}

}
